#include "minishell.h"

int	is_sep(char c)
{
	if (ft_isspace(c) || c == '<' || c == '>' || c == '|' || c == ';'
		|| c == '$' || c == '\'' || c == '\"' || c == '\0')
		return (1);
	return (0);
}
